# all_function.py

# The all() function takes a container as an argument.
# This Built in Function returns True if all values
# in a python iterable have a Boolean value of True.
# An empty value has a Boolean value of False.

# My version of all


# All elements of a given container must be populated in order to get True
print(all({'', 'B', 'D', 'E', 'F'}))
print(all({'A', 'B', 'D', 'E', 'F'}))
print(all(['A', 'B', 'D', 'E', 'F']))
