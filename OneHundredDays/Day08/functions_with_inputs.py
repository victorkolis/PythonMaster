def greet():
    print("Hello there.")
    print("How do you do? ")
    print("Isn't the weather nice today? ")
    
greet()


def greet_with_name(name):
    print(f"\nHello there {name}.")
    print("How do you do? ")
    print("Isn't the weather nice today? ")

greet_with_name("Victor")
