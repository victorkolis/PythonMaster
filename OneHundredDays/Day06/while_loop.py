# while_loop

# Ascending
iterator = 0

while iterator < 3:
    print("Ascending while loop")
    iterator += 1
    
# Descending
iterator = 3

while iterator > 0:
    print("Ascending while loop")
    iterator -= 1

# Negation
iterator = -1

while not iterator > 3:
    iterator += 1
    print(iterator)
