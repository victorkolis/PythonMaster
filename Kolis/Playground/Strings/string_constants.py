import string

alphabet_aZ = string.ascii_letters
print(alphabet_aZ)

alphabet_a = string.ascii_lowercase
print(alphabet_a)

alphabet_A = string.ascii_uppercase
print(alphabet_A)

numbers = string.digits
print(numbers)

hexadecimal_numbers = string.hexdigits
print(hexadecimal_numbers)

octnumbers = string.octdigits
print(octnumbers)

symbols_and_punctuations = string.punctuation
print(symbols_and_punctuations)

printable_characters = string.printable
print(printable_characters)

whitespace = str(string.whitespace)
print(whitespace)  # ' \t\n\r\x0b\x0c'
